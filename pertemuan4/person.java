public class person {
    // atribut
    private String nama, alamat;

    //constructor
    public person(String nama, String alamat) {
        this.nama = nama;
        this.alamat = alamat;
    }

    //getter
    // cara cepat:
    public String getNama(){
        return nama;
    }

    public String getAlamat(){
        return alamat;
    }

    
    
}
